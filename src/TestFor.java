public class TestFor {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            System.out.println(i);
        }

        System.out.println();

        for (int i = 7; i <= 121; i++) {
            System.out.println(i);
        }

        System.out.println();

        for (int i = 100; i >= 30; i--) {
            System.out.println(i);
        }
    }
}
